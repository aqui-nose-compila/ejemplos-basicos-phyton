# Programa en el que se solicita el nombre al usuario (función print()) y se muestra mensaje de saludo

# Guardamos el mensaje en una variable
mensaje = "¿Cómo te llamas?"

# Mostrar el mensaje
print(mensaje)

# Guardar lo introducido por el usuario en una variable
nombre = input()

# Mostrar un saludo que combina texto y la variable nombre
print("Hola, ", nombre, ". Me alegro de conocerte.")
