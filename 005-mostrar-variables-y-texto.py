# Programa que realiza la suma del contenido de dos variables y muestra el resultado
# El resultado lo muestra junto con una cadena de texto

# Declaración y asignación del valor de las variables
numero1 = 4
numero2 = 8

# Guardar la suma en otra variable
suma = numero1 + numero2
# Mostrar el resultado de la suma junto con un texto con la función print
print('La suma de ', numero1, ' y ', numero2, ' es ', suma)
